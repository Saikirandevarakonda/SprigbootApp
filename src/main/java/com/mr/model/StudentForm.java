package com.mr.model;

public class StudentForm {
	private String sid;
	private String sname;
	private String smarks;
	private String spercentage;
	private String sresults;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getSmarks() {
		return smarks;
	}
	public void setSmarks(String smarks) {
		this.smarks = smarks;
	}
	public String getSpercentage() {
		return spercentage;
	}
	public void setSpercentage(String spercentage) {
		this.spercentage = spercentage;
	}
	public String getSresults() {
		return sresults;
	}
	public void setSresults(String sresults) {
		this.sresults = sresults;
	}
	

}
